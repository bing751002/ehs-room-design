export default function AuditPage() {
  const [reportData, setReportData] = useState(null)

  return (
    <>
      <AgentPageLayout
        agentType="audit"
        title="🔍 圖面審查"
        subtitle="跑照建築師 + 防綜顧問"
        systemPrompt={AUDIT_SYSTEM_PROMPT}
        quickPrompts={AUDIT_QUICK_PROMPTS}
        ragOptions={AUDIT_RAG_OPTIONS}
        emptyHint={AUDIT_EMPTY_HINT}
        placeholder="把設計師寄來的圖拖進來,或描述要審查的重點…"
        extraToolbar={({ threadId, threadTitle, messages }) => (
          messages.length > 0 && (
            <button
              onClick={() => setReportData({
                title: threadTitle, agentType: 'audit', messages
              })}
              className="text-xs px-3 py-1.5 rounded bg-slate-700 text-white hover:bg-slate-600">
              🖨 列印審查報告
            </button>
          )
        )}
      />
      {reportData && (
        <PrintableReport
          {...reportData}
          onClose={() => setReportData(null)}
        />
      )}
    </>
  )
}
